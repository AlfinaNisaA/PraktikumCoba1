public class Kendaraan {
    String warna;
    String merk;
    String model;
    int tahun_pembuatan;

    void setWarna(String arg_warna) {
        this.warna = arg_warna;
    }

    void setMerk(String arg_merk) {
        this.merk = arg_merk;
    }

    void setModel(String arg_model) {
        this.model = arg_model;
    }

    void setTahunPembuatan(int arg_tahun_pembuatan) {
        this.tahun_pembuatan = arg_tahun_pembuatan;
    }

}
